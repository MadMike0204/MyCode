;COUT子程序
	
	COUT	ST R0,WHO
		ST R7,RSJ7
		ST R2,WINER
		LD R3,QIPAN2
		ST R3,QIPAN3
		
		AND R5,R5,#0	

	WXH	AND R4,R4,#0	
		ADD R4,R4,#6
		LD R3,QIPAN3
		ADD R3,R5,R3
		
	NXH	LDR R0,R3,#0
		OUT
		ADD R3,R3,#6

		AND R0,R0,#0
		ADD R4,R4,#-1
		BRp #3
		ADD R0,R0,XA
		OUT
		BRnzp HUANH
		LD R0,SP
		OUT
		BRnzp NXH
	HUANH	ADD R5,R5,#1
		ADD R6,R5,#-6
		BRn WXH

		LD R0,WHO
		ADD R0,R0,#-1
		BRz	GAMEOVER
		
		AND R0,R0,#0
	BACK	LD R7,RSJ7
		RET

   GAMEOVER	LD R3,WINER
		LD R2,PLAYER1
		LEA R0,WIN1
		ADD R6,R2,R3	
		BRz #1
		LEA R0,WIN2
		PUTS
		
		AND R0,R0,#0
		ADD R0,R0,#-1
		BRnzp BACK
;COUT数据区
	WHO	.FILL X0
	RSJ7	.FILL X0
	SP	.FILL X20
	QIPAN3	.FILL X0
	PLAYER1	.FILL X4F
	WINER	.FILL X0	;存放或许会赢的玩家的字符
	WIN1	.STRINGZ "Player 1 Wins."
	WIN2	.STRINGZ "Player 2 Wins."